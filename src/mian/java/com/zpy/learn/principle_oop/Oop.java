/**
 * 面向对象的7大原则
 *
 * 1.开闭原则：对拓展开放，对修改关闭
 * 2.李氏替换原则：继承必须保证父类的基本性质在子类仍然成立
 * 3.依赖倒置原则：面向接口编程，降低项目耦合。 不要面向实现编程
 * 4.单一职责原则：控制类的粒度大小，对象解耦，提高其内聚性。
 * 5.接口隔离原则：为各个类建立他们需要的专用接口。提高内聚性。
 * 6.迪米特法则：直接与朋友交谈，不和陌生人交谈。（代理模式）
 * 7.合成复用原则：尽量先使用组合或者聚合等关系来实现，其次考虑使用继承关系实现。
 */
